package org.mytests.uiobjects.example.entities;

public class Range {
    public int from;
    public int to;

    public Range(int from, int to) {
        this.from = from;
        this.to = to;
    }

    public void setRange(int from, int to) {
        this.from = from;
        this.to = to;
    }
}
